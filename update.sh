#!/bin/bash
# ============================================
# 出店者情報更新 → GitHub プッシュスクリプト
# ============================================
# index.html を編集した後、このスクリプトを実行
# ============================================

set -e

echo ""
echo "🐧 ペンギンマルシェ サイト更新"
echo "======================================"

# 変更確認
echo ""
echo "📝 変更内容:"
git diff --stat

echo ""
read -p "コミットメッセージを入力（例: 出店者追加 ○○）: " MSG
if [ -z "$MSG" ]; then
  MSG="サイト更新 $(date '+%Y-%m-%d %H:%M')"
fi

git add -A
git commit -m "$MSG"
git push origin main

echo ""
echo "✅ 更新完了！数分でサイトに反映されます。"
echo ""
